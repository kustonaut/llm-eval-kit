"""Generate interactive HTML reports with charts from eval results."""

from __future__ import annotations

from pathlib import Path
from typing import Any

from llm_eval_kit.scorer import ScoreCard
from llm_eval_kit.eval_runner import SuiteResult
from llm_eval_kit.compare import ComparisonResult


class HTMLReporter:
    """Generate rich HTML reports with Chart.js visualizations.

    Usage:
        reporter = HTMLReporter()
        html = reporter.suite_report(suite_result)
        reporter.save(html, "report.html")
    """

    def _base_template(self, title: str, body: str) -> str:
        return f"""<!DOCTYPE html>
<html lang="en"><head><meta charset="UTF-8"><meta name="viewport" content="width=device-width,initial-scale=1.0">
<title>{title}</title>
<script src="https://cdn.jsdelivr.net/npm/chart.js@4"></script>
<style>
:root{{--bg:#0d1117;--bg2:#161b22;--border:#30363d;--text:#e6edf3;--muted:#8b949e;--blue:#58a6ff;--green:#3fb950;--red:#f85149;--amber:#d29922;--cyan:#39d2c0;}}
*{{box-sizing:border-box;margin:0;padding:0;}}
body{{font-family:'Segoe UI',sans-serif;background:var(--bg);color:var(--text);max-width:1000px;margin:0 auto;padding:2rem;line-height:1.6;}}
h1{{color:var(--blue);font-size:1.6rem;margin-bottom:.5rem;}}
h2{{color:var(--cyan);font-size:1.2rem;margin:2rem 0 .8rem;border-bottom:1px solid var(--border);padding-bottom:.4rem;}}
.summary{{display:grid;grid-template-columns:repeat(auto-fit,minmax(150px,1fr));gap:1rem;margin:1.5rem 0;}}
.stat{{background:var(--bg2);border:1px solid var(--border);border-radius:8px;padding:1rem;text-align:center;}}
.stat-value{{font-size:2rem;font-weight:800;}}
.stat-value.pass{{color:var(--green);}}.stat-value.fail{{color:var(--red);}}.stat-value.neutral{{color:var(--blue);}}
.stat-label{{font-size:.8rem;color:var(--muted);margin-top:.3rem;}}
table{{width:100%;border-collapse:collapse;margin:1rem 0;}}
th{{background:var(--bg2);color:var(--blue);padding:.6rem .8rem;text-align:left;font-size:.82rem;text-transform:uppercase;letter-spacing:.5px;border-bottom:2px solid var(--border);}}
td{{padding:.5rem .8rem;border-bottom:1px solid var(--border);font-size:.9rem;}}
tr:hover td{{background:var(--bg2);}}
.pass-icon{{color:var(--green);}}.fail-icon{{color:var(--red);}}
.chart-container{{background:var(--bg2);border:1px solid var(--border);border-radius:8px;padding:1.5rem;margin:1rem 0;}}
canvas{{max-height:300px;}}
.footer{{margin-top:2rem;padding-top:1rem;border-top:1px solid var(--border);color:var(--muted);font-size:.8rem;text-align:center;}}
</style></head><body>
{body}
<div class="footer">Generated by <a href="https://github.com/kustonaut/llm-eval-kit" style="color:var(--blue);text-decoration:none;">llm-eval-kit</a></div>
</body></html>"""

    def scorecard_report(self, scorecard: ScoreCard, title: str = "Eval Report") -> str:
        status = "pass" if scorecard.passed else "fail"
        checks_html = ""
        labels, scores = [], []
        for c in scorecard.checks:
            icon = '<span class="pass-icon">✅</span>' if c.passed else '<span class="fail-icon">❌</span>'
            findings = "; ".join(c.findings[:3]) if c.findings else "—"
            checks_html += f"<tr><td>{c.name}</td><td>{c.score:.2f}</td><td>{icon}</td><td style='color:var(--muted);font-size:.85rem'>{findings}</td></tr>"
            labels.append(c.name)
            scores.append(c.score)

        body = f"""<h1>{title}</h1>
<div class="summary">
  <div class="stat"><div class="stat-value {status}">{scorecard.overall_score:.2f}</div><div class="stat-label">Overall Score</div></div>
  <div class="stat"><div class="stat-value {'pass' if scorecard.passed else 'fail'}">{'PASS' if scorecard.passed else 'FAIL'}</div><div class="stat-label">Status</div></div>
  <div class="stat"><div class="stat-value neutral">{scorecard.passed_checks}/{scorecard.total_checks}</div><div class="stat-label">Checks Passed</div></div>
</div>
<h2>Check Results</h2>
<table><tr><th>Check</th><th>Score</th><th>Status</th><th>Findings</th></tr>{checks_html}</table>
<div class="chart-container"><canvas id="radarChart"></canvas></div>
<script>
new Chart(document.getElementById('radarChart'),{{
  type:'radar',
  data:{{labels:{labels},datasets:[{{label:'Score',data:{scores},borderColor:'#58a6ff',backgroundColor:'rgba(88,166,255,0.1)',pointBackgroundColor:'#58a6ff'}}]}},
  options:{{scales:{{r:{{min:0,max:1,ticks:{{stepSize:0.2,color:'#8b949e'}},grid:{{color:'#30363d'}},pointLabels:{{color:'#e6edf3'}}}}}},plugins:{{legend:{{display:false}}}}}}
}});
</script>"""
        return self._base_template(title, body)

    def suite_report(self, result: SuiteResult, title: str | None = None) -> str:
        title = title or f"Eval Suite: {result.name}"
        rows_html = ""
        test_names, test_scores = [], []
        for r in result.results:
            icon = '<span class="pass-icon">✅</span>' if r.overall_pass else '<span class="fail-icon">❌</span>'
            findings = "; ".join(r.findings[:2]) if r.findings else "—"
            rows_html += f"<tr><td>{r.test_case.name}</td><td>{r.scorecard.overall_score:.2f}</td><td>{icon}</td><td style='color:var(--muted);font-size:.85rem'>{findings}</td></tr>"
            test_names.append(r.test_case.name)
            test_scores.append(r.scorecard.overall_score)

        colors = ["'#3fb950'" if r.overall_pass else "'#f85149'" for r in result.results]

        body = f"""<h1>{title}</h1>
<div class="summary">
  <div class="stat"><div class="stat-value {'pass' if result.pass_rate >= 0.7 else 'fail'}">{result.pass_rate:.0%}</div><div class="stat-label">Pass Rate</div></div>
  <div class="stat"><div class="stat-value neutral">{result.passed}/{result.total}</div><div class="stat-label">Tests Passed</div></div>
  <div class="stat"><div class="stat-value neutral">{result.avg_score:.2f}</div><div class="stat-label">Avg Score</div></div>
</div>
<h2>Results</h2>
<table><tr><th>Test</th><th>Score</th><th>Status</th><th>Findings</th></tr>{rows_html}</table>
<div class="chart-container"><canvas id="barChart"></canvas></div>
<script>
new Chart(document.getElementById('barChart'),{{
  type:'bar',
  data:{{labels:{test_names},datasets:[{{label:'Score',data:{test_scores},backgroundColor:[{','.join(colors)}],borderRadius:4}}]}},
  options:{{indexAxis:'y',scales:{{x:{{min:0,max:1,grid:{{color:'#30363d'}},ticks:{{color:'#8b949e'}}}},y:{{ticks:{{color:'#e6edf3'}}}}}},plugins:{{legend:{{display:false}}}}}}
}});
</script>"""
        return self._base_template(title, body)

    def comparison_report(self, result: ComparisonResult, title: str = "Model Comparison") -> str:
        rows_html = ""
        model_names, model_scores = [], []
        for rank, mr in enumerate(sorted(result.results, key=lambda x: -x.scorecard.overall_score)):
            medal = "🥇" if rank == 0 else "🥈" if rank == 1 else "🥉" if rank == 2 else str(rank + 1)
            checks_detail = " ".join(f"{c.name}={c.score:.1f}" for c in mr.scorecard.checks)
            rows_html += f"<tr><td>{medal}</td><td>{mr.model_name}</td><td>{mr.scorecard.overall_score:.2f}</td><td>{'✅' if mr.scorecard.passed else '❌'}</td><td style='color:var(--muted);font-size:.8rem'>{checks_detail}</td></tr>"
            model_names.append(mr.model_name)
            model_scores.append(mr.scorecard.overall_score)

        body = f"""<h1>{title}</h1>
<p style="color:var(--muted)">Prompt: {result.prompt[:200]}</p>
<div class="summary">
  <div class="stat"><div class="stat-value pass">{result.winner}</div><div class="stat-label">Winner</div></div>
  <div class="stat"><div class="stat-value neutral">{len(result.results)}</div><div class="stat-label">Models</div></div>
</div>
<h2>Rankings</h2>
<table><tr><th>Rank</th><th>Model</th><th>Score</th><th>Pass</th><th>Checks</th></tr>{rows_html}</table>
<div class="chart-container"><canvas id="compChart"></canvas></div>
<script>
new Chart(document.getElementById('compChart'),{{
  type:'bar',
  data:{{labels:{model_names},datasets:[{{label:'Overall Score',data:{model_scores},backgroundColor:'#58a6ff',borderRadius:4}}]}},
  options:{{scales:{{y:{{min:0,max:1,grid:{{color:'#30363d'}},ticks:{{color:'#8b949e'}}}},x:{{ticks:{{color:'#e6edf3'}}}}}},plugins:{{legend:{{display:false}}}}}}
}});
</script>"""
        return self._base_template(title, body)

    def save(self, content: str, path: str | Path) -> None:
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
